void main() {
  int marks = 39;

  if (marks >= 80) {
    print('আপনি A+ পেয়েছেন');

    if (marks >= 90) {
      print('অসাধারণ! আপনি ৯০ এর উপরে পেয়েছেন');
    } else {
      print('ভালো করেছেন! ৮০-৮৯ এর মধ্যে পেয়েছেন');
    }

  } else if (marks >= 70) {
    print('আপনি A পেয়েছেন');
  } else if (marks >= 60) {
    print('আপনি A- পেয়েছেন');
  } else {
    print('দুঃখিত, আপনি পাশ করতে পারেননি');

    if (marks >= 40) {
      print('কিন্তু আপনি ৪০ এর উপরে পেয়েছেন, সুযোগ আছে!');
    }
  }
}